
package com.example.project;

public class Calculator {

	/**
	* The calculator add() function takes in two integers and returns the sum.
	*
	* This comment will be found in the generated Doxygen documentation!
	*
	* @param a The first integer to be added to determine the sum
	* @param b The second integer to be added to determine the sum
 	*/
	public int add(int a, int b) {
		return a + b;
	}

	// The subtract method
	public int subtract(int a, int b) {
		return a - b;
	}

}
